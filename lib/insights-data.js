// Helper function to get insight by ID
export function getInsightById(id) {
  return insights.find(insight => insight.id === parseInt(id))
}

// Helper function to get related insights
export function getRelatedInsights(currentId, category, limit = 3) {
  return insights
    .filter(insight => insight.id !== parseInt(currentId) && insight.category === category)
    .slice(0, limit)
}